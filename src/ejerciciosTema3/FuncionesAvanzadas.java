package ejerciciosTema3;

public class FuncionesAvanzadas {
    public static void suma(int param1, int param2, int param3) {
        System.out.println(param1 + param2 + param3);
        System.out.println(param1 - param2 - param3);
        System.out.println(param1 * param2 * param3);
        System.out.println(param1 / param2 / param3);
    }
}